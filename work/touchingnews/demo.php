			<h1>Men Walk On Moon</h1>
			<hr/>
			<h2>Astronauts Land On Plain;<br/>Collect Rocks, Plant Flag</h2>
			<hr/>
			<h3>A Powdery Surface Is Closely Explored</h3>
			<hr/>
			<p class="byline">By John Noble Wilford<br/><small>Special to The New York Times</small></p><br/>
			<p>Houston, Monday, July 21--Men have landed and walked on the moon.</p>
			<p>Two Americans, astronauts of Apollo 11, steered their fragile four-legged lunar module safely and smoothly to the historic landing yesterday at 4:17:40 P.M., Eastern daylight time.</p>
			<p>Neil A. Armstrong, the 38-year-old civilian commander, radioed to earth and the mission control room here:</p>
			<p>"Houston, Tranquility Base here. The Eagle has landed."</p>
			<p>The first men to reach the moon--Mr. Armstrong and his co-pilot, Col. Edwin E. Aldrin, Jr. of the Air Force--brought their ship to rest on a level, rock-strewn plain near the southwestern shore of the arid Sea of Tranquility.</p>
			<p>About six and a half hours later, Mr. Armstrong opened the landing craft's hatch, stepped slowly down the ladder and declared as he planted the first human footprint on the lunar crust:</p>
			<p>"That's one small step for man, one giant leap for mankind."</p>
			<p>His first step on the moon came at 10:56:20 P.M., as a television camera outside the craft transmitted his every move to an awed and excited audience of hundreds of millions of people on earth.</p>
			<p>Tentative Steps Test Soil</p>
			<p>Mr. Armstrong's initial steps were tentative tests of the lunar soil's firmness and of his ability to move about easily in his bulky white spacesuit and backpacks and under the influence of lunar gravity, which is one-sixth that of the earth.</p>
			<p>"The surface is fine and powdery," the astronaut reported. "I can pick it up loosely with my toe. It does adhere in fine layers like powdered charcoal to the sole and sides of my boots. I only go in a small fraction of an inch, maybe an eighth of an inch. But I can see the footprints of my boots in the treads in the fine sandy particles.</p>
			<p>After 19 minutes of Mr. Armstrong's testing, Colonel Aldrin joined him outside the craft.</p>
			<p>The two men got busy setting up another television camera out from the lunar module, planting an American flag into the ground, scooping up soil and rock samples, deploying scientific experiments and hopping and loping about in a demonstration of their lunar agility.</p>
			<p>They found walking and working on the moon less taxing than had been forecast. Mr. Armstrong once reported he was "very comfortable."</p>
			<p>And people back on earth found the black-and-white television pictures of the bug- shaped lunar module and the men tramping about it so sharp and clear as to seem unreal, more like a toy and toy-like figures than human beings on the most daring and far- reaching expedition thus far undertaken.</p>
			<p>Nixon Telephones Congratulations</p>
			<p>During one break in the astronauts' work, President Nixon congratulated them from the White House in what, he said, "certainly has to be the most historic telephone call ever made."</p>
			<p>"Because of what you have done," the President told the astronauts, "the heavens have become a part of man's world. And as you talk to us from the Sea of Tranquility it required us to redouble our efforts to bring peace and tranquility to earth.</p>
			<p>"For one priceless moment in the whole history of man all the people on this earth are truly one--one in their pride in what you have done and one in our prayers that you will return safely to earth."</p>
